import type { Directive } from "vue";
import { createApp } from "vue";
import type { QiankunProps } from "vite-plugin-qiankun/dist/helper";
import {
  qiankunWindow,
  renderWithQiankun,
} from "vite-plugin-qiankun/dist/helper";
import { createPinia } from "pinia";
import { Icon } from "@iconify/vue";
import * as ElementPlusIconsVue from "@element-plus/icons-vue";
import App from "./App.vue";
import router from "@/router";
import "@unocss/reset/tailwind.css";
import "@/styles/index.scss";
import "uno.css";
import "element-plus/dist/index.css";
import "@/permission";
import "virtual:svg-icons-register";
import * as directive from "@/directive";
import thirdLogin from "@/utils/thirdLogin";

let root: any;

function render(props: QiankunProps) {
  const { container } = props;
  root = createApp(App);
  const c = container
    ? container.querySelector("#app")
    : document.getElementById("app");

  Object.keys(directive).forEach((key) => {
    root.directive(key, (directive as { [key: string]: Directive })[key]);
  });

  root
    .component("iconify", Icon)
    .use(createPinia())
    .use(router)
    .mount(c);

  // el 图标注册
  for (const [key, component] of Object.entries(ElementPlusIconsVue)) {
    root.component(key, component);
  }
  thirdLogin();
}

// qiankun
renderWithQiankun({
  mount(props) {
    console.log("vue3sub mount");
    render(props);
  },
  bootstrap() {
    console.log("bootstrap");
  },
  unmount() {
    console.log("vue3sub unmount");
    root.unmount();
  },
  update(props: QiankunProps) {
    console.log("vue3sub update");
    console.log(props);
  },
});

if (!qiankunWindow.__POWERED_BY_QIANKUN__) {
  console.log("独立访问");
  render({});
}
